print("#####Ordenar dos numeros de manera ascendente#####")

numA = float(input("Ingresa primer numero: "))
numB = float(input("Ingresa segundo numero:"))

if numA > numB:
    print("Numero Ordenados Ascendentemente: [",numB,",",numA,"]")
else:
    print("Numero Ordenados Ascendentemente: [", numA, ",", numB, "]")